    <br>
    <h3>家庭护理</h3>
    <br>
    <p>新型冠状病毒肺炎存在家庭聚集现象，有疑似症状则及时至传染科就诊，需要在具备有效隔离条件和防护条件的医院隔离治疗。</p>
    <br>
    <hr>
    <br>
    <h3>日常生活管理</h3>
    <br>
    <h4>饮食</h4>
    <br>
    <li>不食用野生动物（即野味），禽、肉、蛋要充分煮熟后食用；</li>
    <li>注意营养、避免偏食、保证摄入食物多样化，尤其是新鲜蔬菜、水果等；</li>
    <li>养成良好饮食习惯，按时按量用餐，避免暴饮暴食。<br></li>
    <br>
    <h4>运动</h4>
    <br>
    <p>积极锻炼，控制体重。此外，适度运动也有助于提高免疫力。</p>
    <br>
    <h4>生活方式</h4>
    <br>
    <li>戒烟、限酒；</li>
    <li>保证充足睡眠、注意休息、避免熬夜；</li>
    <li>关注心理健康，注意缓解紧张情绪、减轻心理压力。<br></li>
    <br>
    <hr>
    <br>
    <h3>日常病情监测</h3>
    <br>
    <p>注意监测体温，如有发热、乏力、咳嗽等症状及时就诊。如发病前两周内有疫源地（武汉市）居住或旅行史、有与确诊或疑似患者接触病史，或者有集体发病表现，需及时就诊并主动隔离。根据《新型冠状病毒肺炎诊疗方案（试行第六版）》出院后注意事项如下：</p>
    <li>定点医院要做好与患者居住地基层医疗机构间的联系，共享病历资料，及时将出院患者信息推送至患者辖区或居住地居委会和基层医疗卫生机构。<br></li>
    <li>患者出院后，因恢复期机体免疫功能低下，有感染其它病原体风险，建议应继续进行14天自我健康状况监测，佩戴口罩，有条件的居住在通风良好的单人房间，减少与家人的近距离密切接触，分餐饮食，做好手卫生，避免外出活动。<br></li>
    <li>建议在出院后第2周、第4周到医院随访、复诊。<br></li>
    <br>
    <hr>
    <br>
    <h3>特殊注意事项</h3>
    <br>
    <li>潜伏期1-14天，多为3-7天。潜伏期具有传染性，无症状感染者也可能成为传染源。如有确诊或疑似患者接触病史，即使暂时没有发热等症状，也需要主动隔离。</li>
    <li>解除隔离需要体温恢复正常3天以上，呼吸道症状明显好转，连续2次（至少间隔1天）病原体检测阴性。<br></li>
    <br>
    <hr>
    <br>
    <h3>预防</h3>
    <br>
    <p>在传染病爆发流行时，采取的措施包括针对病原体、易感人群、传播途径和疫源地等方面的措施。</p>
    <p>新型冠状病毒肺炎暂时没有有效的疫苗，做好个人防护是最切实可行的办法，包括不要去人群聚集处、勤洗手、多饮水、注意休息等。</p>
    <p>具体预防手段如下：</p>
    <li>尽量减少外出，不要去人群聚集处，避免近距离接触任何有感冒或流感样症状的人；</li>
    <li>外出前往公共场所、就医和乘坐交通工具时，注意佩戴医用外科口罩或N95口罩；</li>
    <li>不要接触、购买和食用野生动物（即野味），避免在未加防护的情况下接触野生动物和家禽家畜；</li>
    <li>注意手卫生，勤洗手，使用洗手液或肥皂，流水洗手，或使用含酒精成分的免洗洗手液；</li>
    <li>打喷嚏或咳嗽时不要用手去捂，要用手肘部或纸巾遮住口、鼻；</li>
    <li>居室及工作场所保持清洁，勤开窗，多保持通风状态；</li>
    <li>注意多喝水、多休息、避免熬夜、适度运动，以提高个体免疫能力；注意营养、合理饮食，肉类、禽类和蛋类要充分煮熟后食用；</li>
    <li>准备常用物资，如体温计、一次性口罩、家庭用消毒用品等。</li>
    
