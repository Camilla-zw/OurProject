    <br>
    <h3>基本病因</h3>
    <br>
    <p>冠状病毒属于单股正链RNA病毒。既往已知感染人的冠状病毒有6种，即HCoV-229E、HCoV-OC43、SARSr-CoV、HCoV-NL63、HCoV-HKU1和MERSr-CoV。</p>
    <br>
